// Reference Data Types

//Object

// An object is a collection of properties.

let person = {
    firstName : 'Elon',
    lastName : 'Millo',
    age : 35
};

console.log(person);
console.log(typeof person);

//Array
// stores a collection of values.

let number2 = [1, 2, 3, 4, 5];

console.log(number2)
console.log(typeof number2)


// function Data types.
// functions are a type of object that can be used to execute code
function msg(){
    console.log("Hello , World")
}
console.log(msg)
console.log(typeof msg)

// JavaScript is a dynamically typed langauge.
// We can store different date-types in the same variable 

let z;
console.log(z);
console.log(typeof z);

z = 'Helloz!'

console.log(z)

z = 100;
console.log(z)
console.log(typeof z);


